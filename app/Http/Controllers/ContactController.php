     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'subject' => 'required',
            'message' => 'required',

        ]);

        $data = $request->all();
        Contact::create($data);


        return redirect(url()->previous())->with('message', 'your message has been sent successfully.');
    }

